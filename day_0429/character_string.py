# 字符串

# 字符串拼接
a = '果芽'
b = '测开'
c = '学院'
print(a + b + c)  # 使用+来拼接字符串 （推荐）
a = "果芽" "测开" "学院"  # 使用连续书写的方式来拼接字符串，注意这种写法只能拼接常量。（不推荐）
print(a)

# 字符串和其他类型拼接
a = "我是周杰伦，今年" + str(18) + "岁了"  # 两者任选其一即可
b = "我是周杰伦，今年" + repr(18) + "岁了"# 两者任选其一即可
print(a)
print(b)

# split切割字符串
txt = "Google#Runoob#Taobao#Facebook"
# 第二个参数为 1，返回两个参数列表
x = txt.split("#", 1)
print(x)